# EJEMPLO PARA ACCERDER A LOS ELEMENTOS DE UNA LISTA:

lista = ['a', 'b', 'd', 'i', 'j']

lista[0] # Primer elemento de la lista. Índice 0
print(lista[0]) # Imprime 
'a'

lista[3] # Cuarto elemento de la lista. Índice 3
print(lista[3]) # Imprime:
'i'

# EJEMPLO PARA ACCERDER A LOS ELEMENTOS DE UNA LISTA USANDO UN INDICE NEGATIVO.

lista = ['a', 'b', 'd', 'i', 'j']

lista[-1] # Último elemento de la lista. Índice -1
print(lista[-1]) # Imprime:
'j'

lista[-3] # Tercer elemento de la lista de forma inversa. Índice -3
print(lista[-3]) # Imprime:
'd'



